export const APP_LANGUAGE_COPY = Object.fromEntries(
  APP_LANGUAGES.map((language) => [language, createCopy(COPY_SOURCES[language])]),
) as Record<AppLanguage, AppCopy>;

export function getAppCopy(language: AppLanguage): AppCopy {
  return APP_LANGUAGE_COPY[language] ?? APP_LANGUAGE_COPY[DEFAULT_APP_LANGUAGE];
}

export function getAppLanguageLabel(language: AppLanguage): string {
  return APP_LANGUAGE_META[language].label;
}

export function getAppLanguageCode(language: AppLanguage): string {
  return APP_LANGUAGE_META[language].code;
}

export function getAppLanguageDirection(language: AppLanguage): LanguageDirection {
  return APP_LANGUAGE_META[language].direction;
}

export function isAppLanguage(value: unknown): value is AppLanguage {
  return typeof value === "string" && APP_LANGUAGES.includes(value as AppLanguage);
}

export function isTranslationAppLanguage(language: AppLanguage): language is SupportedTranslationLanguage {
  return language !== DEFAULT_APP_LANGUAGE;
}
